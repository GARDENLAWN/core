    &lt;h2 id="sec5" class="polityka-h2"&gt;5. Profilowanie w sklepie internetowym&lt;/h2&gt;
    &lt;p class="polityka-p"&gt;&lt;strong&gt;5.1.&lt;/strong&gt; Rozporządzenie RODO nakłada na Administratora obowiązek informowania o
        zautomatyzowanym podejmowaniu decyzji, w tym o profilowaniu, o którym mowa w art. 22 ust. 1 i 4 Rozporządzenia
        RODO, oraz – przynajmniej w tych przypadkach – istotne informacje o zasadach ich podejmowania, a także o
        znaczeniu i przewidywanych konsekwencjach takiego przetwarzania dla osoby, której dane dotyczą. Mając to na
        uwadze, Administrator podaje w tym punkcie polityki prywatności informacje dotyczące możliwego profilowania.&lt;/p&gt;
    &lt;p class="polityka-p"&gt;&lt;strong&gt;5.2.&lt;/strong&gt; Administrator może korzystać w Sklepie Internetowym z profilowania do
        celów marketingu bezpośredniego, ale decyzje podejmowane na jego podstawie przez Administratora nie dotyczą
        zawarcia lub odmowy zawarcia Umowy Sprzedaży czy też możliwości korzystania z Usług Elektronicznych w Sklepie
        Internetowym. Efektem korzystania z profilowania w Sklepie Internetowym może być np. przyznanie danej osobie
        rabatu, przesłanie jej kodu rabatowego, przypomnienie o niedokończonych zakupach, przesłanie propozycji
        Produktu, który może odpowiadać zainteresowaniom lub preferencjom danej osoby lub też zaproponowanie lepszych
        warunków w porównaniu do standardowej oferty Sklepu Internetowego. Mimo profilowania to dana osoba podejmuje
        swobodnie decyzję, czy będzie chciała skorzystać z otrzymanego w ten sposób rabatu, czy też lepszych warunków i
        dokonać zakupu w Sklepie Internetowym.&lt;/p&gt;
    &lt;p class="polityka-p"&gt;&lt;strong&gt;5.3.&lt;/strong&gt; Profilowanie w Sklepie Internetowym polega na automatycznej analizie lub
        prognozie zachowania danej osoby na stronie Sklepu Internetowego np. poprzez dodanie konkretnego Produktu do
        koszyka, przeglądanie strony konkretnego Produktu w Sklepie Internetowym czy też poprzez analizę dotychczasowej
        historii dokonanych zakupów w Sklepie Internetowym. Warunkiem takiego profilowania jest posiadanie przez
        Administratora danych osobowych danej osoby, aby móc jej następnie przesłać np. kod rabatowy.&lt;/p&gt;
    &lt;p class="polityka-p"&gt;&lt;strong&gt;5.4.&lt;/strong&gt; Osoba, której dane dotyczą, ma prawo do tego, by nie podlegać decyzji,
        która opiera się wyłącznie na zautomatyzowanym przetwarzaniu, w tym profilowaniu, i wywołuje wobec tej osoby
        skutki prawne lub w podobny sposób istotnie na nią wpływa.&lt;/p&gt;
