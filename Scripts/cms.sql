        roszczenia, jak również za wszelkie roszczenia w sumie – do wysokości zapłaconej ceny oraz kosztów dostawy z
        tytułu Umowy Sprzedaży, nie więcej jednak niż do kwoty jednego tysiąca złotych. Ograniczenie kwotowe, o którym
        mowa w zdaniu poprzednim, ma zastosowanie do wszelkich roszczeń kierowanych przez Usługobiorcę/Klienta w
        stosunku do Usługodawcy/Sprzedawcy, w tym także w przypadku braku zawarcia Umowy Sprzedaży lub niezwiązanych z
        Umową Sprzedaży. Usługodawca/Sprzedawca ponosi odpowiedzialność w stosunku do Usługobiorcy/Klienta tylko za
        typowe szkody przewidywalne w momencie zawarcia umowy i nie ponosi odpowiedzialności z tytułu utraconych
        korzyści. Sprzedawca nie ponosi także odpowiedzialności za opóźnienie w przewozie przesyłki.&lt;/p&gt;
    &lt;p class="regulamin-p"&gt;&lt;strong&gt;9.8.&lt;/strong&gt; Wszelkie spory powstałe pomiędzy Sprzedawcą/Usługodawcą a
        Klientem/Usługobiorcą zostają poddane sądowi właściwemu ze względu na siedzibę Sprzedawcy/Usługodawcy.&lt;/p&gt;
