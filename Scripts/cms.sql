    &lt;h2 id="sec2" class="polityka-h2"&gt;2. Podstawy przetwarzania danych&lt;/h2&gt;
    &lt;p class="polityka-p"&gt;&lt;strong&gt;2.1.&lt;/strong&gt; Administrator uprawniony jest do przetwarzania danych osobowych w
        przypadkach, gdy – i w takim zakresie, w jakim – spełniony jest co najmniej jeden z poniższych warunków: (1)
        osoba, której dane dotyczą wyraziła zgodę na przetwarzanie swoich danych osobowych w jednym lub większej liczbie
        określonych celów; (2) przetwarzanie jest niezbędne do wykonania umowy, której stroną jest osoba, której dane
        dotyczą, lub do podjęcia działań na żądanie osoby, której dane dotyczą, przed zawarciem umowy; (3) przetwarzanie
        jest niezbędne do wypełnienia obowiązku prawnego ciążącego na Administratorze; lub (4) przetwarzanie jest
        niezbędne do celów wynikających z prawnie uzasadnionych interesów realizowanych przez Administratora lub przez
        stronę trzecią, z wyjątkiem sytuacji, w których nadrzędny charakter wobec tych interesów mają interesy lub
        podstawowe prawa i wolności osoby, której dane dotyczą, wymagające ochrony danych osobowych, w szczególności gdy
        osoba, której dane dotyczą, jest dzieckiem.&lt;/p&gt;
    &lt;p class="polityka-p"&gt;&lt;strong&gt;2.2.&lt;/strong&gt; Przetwarzanie danych osobowych przez Administratora wymaga każdorazowo
        zaistnienia co najmniej jednej z podstaw wskazanych w pkt. 2.1 polityki prywatności. Konkretne podstawy
        przetwarzania danych osobowych Usługobiorców i Klientów Sklepu Internetowego przez Administratora są wskazane w
        kolejnym punkcie polityki prywatności – w odniesieniu do danego celu przetwarzania danych osobowych przez
        Administratora.&lt;/p&gt;
