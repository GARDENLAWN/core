        sprostowania, usunięcia („prawo do bycia zapomnianym”) lub ograniczenia przetwarzania oraz ma prawo do
        wniesienia sprzeciwu wobec przetwarzania, a także ma prawo do przenoszenia swoich danych. Szczegółowe warunki
        wykonywania wskazanych wyżej praw wskazane są w art. 15-21 Rozporządzenia RODO.&lt;/p&gt;
    &lt;p class="polityka-p"&gt;&lt;strong&gt;6.2. Prawo do cofnięcia zgody w dowolnym momencie&lt;/strong&gt; – osoba, której dane
        przetwarzane są przez Administratora na podstawie wyrażonej zgody (na podstawie art. 6 ust. 1 lit. a) lub art. 9
        ust. 2 lit. a) Rozporządzenia RODO), to ma ona prawo do cofnięcia zgody w dowolnym momencie bez wpływu na
        zgodność z prawem przetwarzania, którego dokonano na podstawie zgody przed jej cofnięciem.&lt;/p&gt;
    &lt;p class="polityka-p"&gt;&lt;strong&gt;6.3. Prawo wniesienia skargi do organu nadzorczego&lt;/strong&gt; – osoba, której dane
        przetwarzane są przez Administratora, ma prawo wniesienia skargi do organu nadzorczego w sposób i trybie
        określonym w przepisach Rozporządzenia RODO oraz prawa polskiego, w szczególności ustawy o ochronie danych
        osobowych. Organem nadzorczym w Polsce jest Prezes Urzędu Ochrony Danych Osobowych.&lt;/p&gt;
    &lt;p class="polityka-p"&gt;&lt;strong&gt;6.4. Prawo do sprzeciwu&lt;/strong&gt; – osoba, której dane dotyczą, ma prawo w dowolnym
        momencie wnieść sprzeciw – z przyczyn związanych z jej szczególną sytuacją – wobec przetwarzania dotyczących jej
        danych osobowych opartego na art. 6 ust. 1 lit. e) (interes lub zadania publiczne) lub f) (prawnie uzasadniony
        interes administratora), w tym profilowania na podstawie tych przepisów. Administratorowi w takim przypadku nie
        wolno już przetwarzać tych danych osobowych, chyba że wykaże on istnienie ważnych prawnie uzasadnionych podstaw
        do przetwarzania, nadrzędnych wobec interesów, praw i wolności osoby, której dane dotyczą, lub podstaw do
        ustalenia, dochodzenia lub obrony roszczeń.&lt;/p&gt;
    &lt;p class="polityka-p"&gt;&lt;strong&gt;6.5. Prawo do sprzeciwu dot. marketingu bezpośredniego&lt;/strong&gt; – jeżeli dane osobowe
        są przetwarzane na potrzeby marketingu bezpośredniego, osoba, której dane dotyczą, ma prawo w dowolnym momencie
        wnieść sprzeciw wobec przetwarzania dotyczących jej danych osobowych na potrzeby takiego marketingu, w tym
        profilowania, w zakresie, w jakim przetwarzanie jest związane z takim marketingiem bezpośrednim.&lt;/p&gt;
    &lt;p class="polityka-p"&gt;&lt;strong&gt;6.6.&lt;/strong&gt; W celu realizacji uprawnień, o których mowa w niniejszym punkcie
        polityki prywatności, można kontaktować się z Administratorem poprzez przesłanie stosownej wiadomości pisemnie
