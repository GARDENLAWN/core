            Sprzedawca świadczy dodatkowo inne usługi niż te, których wykonania konsument żądał, lub dostarcza Produkty
            - rzeczy ruchome (w tym rzeczy ruchome z elementami cyfrowymi) -,inne niż części zamienne niezbędne do
            wykonania naprawy lub konserwacji, prawo odstąpienia od umowy przysługuje konsumentowi w odniesieniu do
            dodatkowych usług lub Produktów; (7) w której przedmiotem świadczenia są nagrania dźwiękowe lub wizualne
