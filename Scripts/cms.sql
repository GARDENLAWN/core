        przyspieszy rozpatrzenie reklamacji. Wymogi podane w zdaniu poprzednim mają formę jedynie zalecenia i nie
        wpływają na skuteczność reklamacji złożonych z pominięciem zalecanego opisu reklamacji.&lt;/p&gt;
    &lt;p class="regulamin-p"&gt;&lt;strong&gt;6.5.&lt;/strong&gt; W razie zmiany podanych danych kontaktowych przez składającego
        reklamację w trakcie rozpatrywania reklamacji zobowiązany jest on do powiadomienia o tym Sprzedawcy.&lt;/p&gt;
    &lt;p class="regulamin-p"&gt;&lt;strong&gt;6.6.&lt;/strong&gt; Do reklamacji mogą zostać załączone przez składającego reklamację
        dowody (np. zdjęcia, dokumenty lub Produkt) związane z przedmiotem reklamacji. Sprzedawca może także zwrócić się
        do składającego reklamację z prośbą o podanie dodatkowych informacji lub przesłanie dowodów (np. zdjęcia),
        jeżeli ułatwi to i przyspieszy rozpatrzenie reklamacji przez Sprzedawcę.&lt;/p&gt;
    &lt;p class="regulamin-p"&gt;&lt;strong&gt;6.7.&lt;/strong&gt; Sprzedawca ustosunkuje się do reklamacji niezwłocznie, nie później niż
        w terminie 14 dni kalendarzowych od dnia jej otrzymania.&lt;/p&gt;
    &lt;p class="regulamin-p"&gt;&lt;strong&gt;6.8.&lt;/strong&gt; Podstawa i zakres odpowiedzialności ustawowej Sprzedawcy są określone
        powszechnie obowiązującymi przepisami prawa, w szczególności w Kodeksie Cywilnym, Ustawie o Prawach Konsumenta
        oraz ustawie o świadczeniu usług drogą elektroniczną z dnia 18 lipca 2002 r. (Dz.U. Nr 144, poz. 1204 ze zm.).
        Poniżej wskazane są dodatkowe informacje dotyczące przewidzianej przez prawo odpowiedzialności Sprzedawcy za
        zgodność Produktu z Umową Sprzedaży:&lt;/p&gt;
    &lt;ul class="regulamin-ul"&gt;
        &lt;li class="regulamin-li"&gt;6.8.1. W przypadku reklamacji &lt;strong&gt;Produktu – rzeczy ruchomej&lt;/strong&gt; – zakupionego
            przez Klienta na podstawie Umowy Sprzedaży zawartej ze Sprzedawcą &lt;strong&gt;do dnia 31. grudnia 2022
                r.&lt;/strong&gt; określają przepisy Kodeksu Cywilnego w brzmieniu obowiązującym do dnia 31. grudnia 2022 r.,
            w szczególności art. 556-576 Kodeksu Cywilnego. Przepisy te określają w szczególności podstawę i zakres
            odpowiedzialności Sprzedawcy względem Klienta, jeżeli sprzedany Produkt ma wadę fizyczną lub prawną
            (rękojmia). Zgodnie z art. 558 § 1 Kodeksu Cywilnego odpowiedzialność Sprzedawcy z tytułu rękojmi za Produkt
            zakupiony zgodnie ze zdaniem poprzednim wobec Klienta niebędącego konsumentem zostaje wyłączona.
        &lt;/li&gt;
        &lt;li class="regulamin-li"&gt;6.8.2. W przypadku reklamacji &lt;strong&gt;Produktu – rzeczy ruchomej (w tym rzeczy ruchomej
            z elementami cyfrowymi)&lt;/strong&gt;, z wyłączeniem jednak rzeczy ruchomej, która służy wyłącznie jako nośnik
            treści cyfrowej – zakupionego przez Klienta na podstawie Umowy Sprzedaży zawartej ze Sprzedawcą &lt;strong&gt;od
                dnia 1. stycznia 2023 r.&lt;/strong&gt; określają przepisy Ustawy o Prawach Konsumenta w brzmieniu
            obowiązującym od dnia 1. stycznia 2023 r., w szczególności art. 43a - 43g Ustawy o Prawach Konsumenta.
            Przepisy te określają w szczególności podstawę i zakres odpowiedzialności Sprzedawcy względem konsumenta, w
            razie braku zgodności Produktu z Umową Sprzedaży.
        &lt;/li&gt;
    &lt;/ul&gt;
    &lt;p class="regulamin-p"&gt;&lt;strong&gt;6.9.&lt;/strong&gt; Poza odpowiedzialnością ustawową na Produkt może zostać udzielona
        gwarancja – jest to odpowiedzialność umowna (dodatkowa) i można z niej skorzystać, gdy dany Produkt jest objęty
        gwarancją. Gwarancja może być udzielona przez inny podmiot niż Sprzedawca (np. przez producenta lub
        dystrybutora). Szczegółowe warunki dotyczące odpowiedzialności na podstawie gwarancji, w tym także dane podmiotu
        odpowiedzialnego za realizację gwarancji oraz podmiotu uprawnionego do skorzystania z niej są dostępne w opisie
        gwarancji, np. w karcie gwarancyjnej lub w innym miejscu dotyczącym udzielenia gwarancji. Sprzedawca wskazuje,
        że w przypadku braku zgodności Produktu z umową Klientowi z mocy prawa przysługują środki ochrony prawnej ze
        strony i na koszt Sprzedawcy oraz że gwarancja nie ma wpływu na te środki ochrony prawnej.&lt;/p&gt;
    &lt;p class="regulamin-p"&gt;&lt;strong&gt;6.10.&lt;/strong&gt; Zawarte w punkcie 6.8.2. Regulaminu postanowienia dotyczące konsumenta
